# Tuplas não aceitam novos valores

# Criação de tuplas
sites = ('youtube.com', 'facebook.com', 'instagram.com')
valores = (23,43,65)

print(sites[1])
print(valores[2])

# União de tuplas 
dados_do_sistema = sites + valores
print(dados_do_sistema)

# Acesso de valores em uma tupla
print(dados_do_sistema[0])